'''
Numerical data types:
- Integers (int): Whole numbers only, no decimal points
    - Can be positive or negative
    - Can be small or really huge
    - From Python3, integers are unbounded (can be as large as we want)
    - i.e: 9, 378, -21, 1234567897
- Floats / Floating point numbers (float): Numbers that have a decimal point.
    - Can be positive or negative
    - They are not unbounded, they are not just infinitely precise.
    - i.e: 1.5, 9.999999, -2.0, -.2, 1.23e12
'''

# Integers
print(type(6)) # int

# Floats
print(type(6.2)) # float

# Floats being not unbounded
print(2.33333333333333333333333333333) # 2.3333333333333335
