'''
Strings: they are textual data types. 
- We can see them as strings of characters.
- Another basic data type in Python (separate from numerical floats and integers).
- Can be represented in double or single quotes.
    - You need to be consistent --> You cannot open with single and close 
                                    with double quotes.
- i.e: color = "Magenta" // color = 'Magenta'
'''
print(type("anything")) # <class 'str'>
print(type("7")) # <class 'str'>
print(type(7)) # <class 'int'>